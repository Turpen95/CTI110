import turtle
def main():
    turtle.showturtle()
    turtle.bgcolor('grey')
    turtle.pensize(2)
    turtle.fillcolor('white')
    turtle.begin_fill()
    turtle.right(45)
    turtle.forward(200)
    turtle.left(90)
    turtle.forward(200)
    turtle.left(90)
    turtle.forward(200)
    turtle.left(90)
    turtle.forward(400)
    turtle.right(90)
    turtle.forward(200)
    turtle.right(90)
    turtle.forward(200)
    turtle.right(90)
    turtle.forward(200)
    turtle.end_fill()

main()

